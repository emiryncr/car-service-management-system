    private void backupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backupBtnActionPerformed
        // TODO add your handling code here:
        try {
            Customer.backupCustomersFromDB();
            Vehicle.backupVehiclesFromDB();
            Inventory.backupInventoryFromDB();
            ServiceOrder.backupServiceOrdersFromDB();
            OrderItem.backupOrderItemsFromDB();
            JOptionPane.showMessageDialog(this, "All data backed up successfully.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Backup failed: " + e.getMessage());
        }
    }//GEN-LAST:event_backupBtnActionPerformed

    private void restoreBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_restoreBtnActionPerformed
        // TODO add your handling code here:
        try {
            Customer.restoreCustomersToDBFromFile();
            Vehicle.restoreVehiclesToDBFromFile();
            Inventory.restoreInventoryToDBFromFile();
            ServiceOrder.restoreServiceOrdersToDBFromFile();
            OrderItem.restoreOrderItemsToDBFromFile();
            JOptionPane.showMessageDialog(this, "All data restored successfully.");
            updateAllTables();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Restore failed: " + e.getMessage());
        }
    }//GEN-LAST:event_restoreBtnActionPerformed

    private void deleteAllBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAllBtnActionPerformed
        // TODO add your handling code here:
        try {
            OrderItem.deleteAllOrderItemsFromDB();
            ServiceOrder.deleteAllServiceOrdersFromDB();
            Inventory.deleteAllInventoryFromDB();
            Vehicle.deleteAllVehiclesFromDB();
            Customer.deleteAllCustomersFromDB();           
            JOptionPane.showMessageDialog(this, "All data deleted successfully.");
            updateAllTables();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "DSeletion failed: " + e.getMessage());
        }
        
    }//GEN-LAST:event_deleteAllBtnActionPerformed

    private void updateAllTables() {
        customerPanel.updateTable();
        vehiclePanel.updateTable();
        inventoryPanel.updateTable();
        serviceOrderPanel.updateTable();
        orderItemPanel.updateTable();
    }
    
