    public void updateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 
        //Map.Entry<Integer, OrderItem> entry : OrderItem.get_all_order_items().entrySet()
        ArrayList<OrderItem> orderItems = OrderItem.getAllOrderItemsFromDB();
        for (OrderItem o : orderItems) {
            //OrderItem o = entry.getValue();

            Object[] row = {
                o.get_order_item_id(),
                o.get_order_id(),
                o.get_part_id(),
                o.get_service_description(),
                o.get_quantity(),
                o.get_unit_price(),
                o.get_line_total()
            };
            model.addRow(row);
        }
    }
