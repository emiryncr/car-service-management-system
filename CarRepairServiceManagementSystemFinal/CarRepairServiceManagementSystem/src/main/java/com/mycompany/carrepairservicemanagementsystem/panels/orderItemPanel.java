    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        try {
            int orderItemId = Integer.parseInt(txtItemId.getText());
            int orderId = Integer.parseInt(txtOrderId.getText());
            int partId = Integer.parseInt(txtPartID.getText());
            String serviceDescription = txtServDesc.getText();
            int quantity = Integer.parseInt(txtQuantity.getText());
            float unitPrice = Float.parseFloat(txtUnitPrice.getText());

            if (serviceDescription.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Service description cannot be empty.");
                return;
            }

            //OrderItem.add_order_item(orderItemId, orderId, partId, serviceDescription, quantity, unitPrice);

            float lineTotal = OrderItem.calculate_line_total(quantity, unitPrice);
            boolean dbSuccess = OrderItem.addOrderItemToDB(orderItemId, orderId, partId, 
                                                         serviceDescription, quantity, 
                                                         unitPrice, lineTotal);
            if (!dbSuccess) {
                JOptionPane.showMessageDialog(this, "Failed to save order item to database!");
            }
            
            updateTable(); 
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid number format: " + ex.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Something went wrong: " + e.getMessage());
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtItemId.getText());
            //OrderItem existing = OrderItem.get_order_item_by_id(id);

            OrderItem existing = OrderItem.getOrderItemFromDB(id);
            if (existing == null) {
                JOptionPane.showMessageDialog(this, "Order item not found!");
                return;
            }

            int orderId = existing.get_order_id();
            int partId = Integer.parseInt(txtPartID.getText());
            String serviceDescription = txtServDesc.getText().isEmpty() ? existing.get_service_description() : txtServDesc.getText();
            int quantity = txtQuantity.getText().isEmpty() ? existing.get_quantity() : Integer.parseInt(txtQuantity.getText());
            float unitPrice = txtUnitPrice.getText().isEmpty() ? existing.get_unit_price() : Float.parseFloat(txtUnitPrice.getText());

            float lineTotal = quantity * unitPrice;
            //OrderItem.edit_order_item(id, orderId, partId, serviceDescription, quantity, unitPrice, lineTotal);
            
            boolean dbSuccess = OrderItem.editOrderItemInDB(id, orderId, partId, 
                                              serviceDescription, quantity, 
                                              unitPrice, lineTotal);
            if (!dbSuccess) {
                JOptionPane.showMessageDialog(this, "Failed to update order item in database!");
            }
            
            updateTable();
            JOptionPane.showMessageDialog(this, "Order item updated successfully.");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid number format: " + ex.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Something went wrong: " + e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

