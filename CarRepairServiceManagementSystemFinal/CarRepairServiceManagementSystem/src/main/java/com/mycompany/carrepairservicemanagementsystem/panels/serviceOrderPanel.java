    public void updateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 
        //Map.Entry<Integer, ServiceOrder> entry : ServiceOrder.get_all_services().entrySet()
        ArrayList<ServiceOrder> serviceOrders = ServiceOrder.getAllServiceOrdersFromDB();
        for (ServiceOrder s : serviceOrders) {
            //ServiceOrder s = entry.getValue();

            Object[] row = {
                s.get_order_id(),
                s.get_vehicle_id(),
                s.get_date_received(),
                s.get_date_completed(),
                s.get_status(),
                s.get_description(),
                s.get_total_cost()
            };
            model.addRow(row);
        }
    }
