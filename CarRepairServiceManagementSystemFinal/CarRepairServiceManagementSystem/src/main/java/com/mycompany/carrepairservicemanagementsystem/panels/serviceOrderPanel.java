    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtOrderId.getText());
            //ServiceOrder existing = ServiceOrder.get_service_by_id(id);
            ServiceOrder existing = ServiceOrder.getServiceOrderFromDB(id);
            if (existing == null) {
                JOptionPane.showMessageDialog(this, "Service order not found!");
                return;
            }
            
            int vId = existing.get_vehicle_id();
                    
            Date received = existing.get_date_received(); 
            String selectedStatus = txtStatusCheck.getSelectedItem().toString();
            String status = selectedStatus.isEmpty() ? existing.get_status() : selectedStatus;

            Date completed = status.equals("Completed") ? new Date() : existing.get_date_completed();

            
            String desc = txtDesc.getText().isEmpty() ? existing.get_description() : txtDesc.getText();
            float cost = txtTotalCost.getText().isEmpty() ? existing.get_total_cost(): Float.parseFloat(txtTotalCost.getText());

            //ServiceOrder.edit_service_order(id, vId, received, completed, status, desc, cost);
            boolean dbSuccess = ServiceOrder.editServiceOrderInDB(id, vId, received, completed, status, desc, cost);
            if (!dbSuccess) {
                JOptionPane.showMessageDialog(this, "Failed to update service order in database!");
            }
            updateTable();
            JOptionPane.showMessageDialog(this, "Service order updated!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Something went wrong: " + e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

