    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
            try {
            int id = Integer.parseInt(txtPartId.getText());
            //Inventory existing = Inventory.get_inventory_by_id(id);
            Inventory existing = Inventory.getInventoryFromDB(id);
            if (existing == null) {
                JOptionPane.showMessageDialog(this, "Inventory item not found!");
                return;
            }

            String iName = txtPartName.getText().isEmpty() ? existing.get_part_name(): txtPartName.getText();
            String iPartNo = txtPartNo.getText().isEmpty() ? existing.get_part_number(): txtPartNo.getText();
            String iCat = txtCategory.getText().isEmpty() ? existing.get_category(): txtCategory.getText();
            int iStock = txtStock.getText().isEmpty() ? existing.get_quantity_in_stock(): Integer.parseInt(txtStock.getText());

            //Inventory.edit_inventory_item(id,iName,iPartNo,iCat,iStock);
            boolean dbSuccess = Inventory.editInventoryInDB(id, iName, iPartNo, iCat, iStock);
            if (!dbSuccess) {
                JOptionPane.showMessageDialog(this, "Failed to update inventory item in database!");
            }
            
            updateTable();

        JOptionPane.showMessageDialog(this, "Inventory updated!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Something went wrong: " + e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

