    public void updateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 
        
        //Map.Entry<Integer, Inventory> entry : Inventory.get_all_inventories().entrySet()
        ArrayList<Inventory> inventoryItems = Inventory.getAllInventoryFromDB();
        for (Inventory i : inventoryItems) {
            //Inventory i = entry.getValue();

            Object[] row = {
                i.get_part_id(),
                i.get_part_name(),
                i.get_part_number(),
                i.get_category(),
                i.get_quantity_in_stock()
            };
            model.addRow(row);
        }
    }
