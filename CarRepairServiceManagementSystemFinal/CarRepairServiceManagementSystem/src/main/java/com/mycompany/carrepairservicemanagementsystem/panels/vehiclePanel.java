    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(txtVehicleId.getText());
            //Vehicle existing = Vehicle.get_vehicle_by_id(id);
            Vehicle existing = Vehicle.getVehicleFromDB(id);
            if (existing == null) {
                JOptionPane.showMessageDialog(this, "Vehicle not found!");
                return;
            }

            int cId = existing.get_customer_id();
            String vMake = txtMake.getText().isEmpty() ? existing.get_make(): txtMake.getText();
            String vModel = txtModel.getText().isEmpty() ? existing.get_model(): txtModel.getText();
            int vYear = txtYear.getText().isEmpty() ? existing.get_year() : Integer.parseInt(txtYear.getText());
            String vVin = txtVin.getText().isEmpty() ? existing.get_vin(): txtVin.getText();
            String vPlate = txtPlate.getText().isEmpty() ? existing.get_license_plate(): txtPlate.getText();
            String vColor = txtColor.getText().isEmpty() ? existing.get_color(): txtColor.getText();

            //Vehicle.edit_vehicle(id,cId,vMake,vModel,vYear,vVin,vPlate,vColor);
            boolean dbSuccess = Vehicle.editVehicleInDB(id, cId, vMake, vModel, vYear, vVin, vPlate, vColor);
            if (!dbSuccess) {
                JOptionPane.showMessageDialog(this, "Failed to save vehicle to database!");
            }
            updateTable();

        JOptionPane.showMessageDialog(this, "Vehicle updated!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Something went wrong: " + e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

