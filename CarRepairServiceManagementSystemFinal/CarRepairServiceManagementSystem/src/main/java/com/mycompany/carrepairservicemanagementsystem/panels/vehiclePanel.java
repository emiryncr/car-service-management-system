    public void updateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 
        //Map.Entry<Integer, Vehicle> entry : Vehicle.get_all_vehicles().entrySet()
        ArrayList<Vehicle> vehicles = Vehicle.getAllVehiclesFromDB();
        for (Vehicle v : vehicles) {
            //Vehicle v = entry.getValue();

            Object[] row = {
                v.get_vehicle_id(),
                v.get_customer_id(),
                v.get_make(),
                v.get_model(),
                v.get_year(),
                v.get_vin(),
                v.get_license_plate(),
                v.get_color()
            };
            model.addRow(row);
        }
    }
